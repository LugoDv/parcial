package model;

/**
 *
 * @author lugo
 */
public class Reservation {
    private int id;
    private String date;
    private String status;
    private User user;
    public Reservation(int id, String date, String status, User user) {
        this.id = id;
        this.date = date;
        this.status = status;
        this.user=user; 
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
    
    
}
